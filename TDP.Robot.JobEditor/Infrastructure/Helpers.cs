using System.Drawing;
using System.IO;
using System.Reflection;

namespace TDP.Robot.JobEditor.Infrastructure
{
    static class Helpers
    {
        public static string PathAppendBackSlash(string path)
        {
            if (path.Length > 0)
            {
                if (path[path.Length - 1] != Path.DirectorySeparatorChar)
                    return path + Path.DirectorySeparatorChar;
            }

            return path;
        }

        public static void CallPrivateMethod(object o, string method, params object[] methodParams)
        {
            MethodInfo dynMethod = o.GetType().GetMethod(method, BindingFlags.NonPublic | BindingFlags.Instance);
            dynMethod.Invoke(o, methodParams);
        }

        /*
        https://stackoverflow.com/questions/5514366/how-to-know-if-a-line-intersects-a-rectangle
         */
        public static bool LineIntersectsRect(Point p1, Point p2, Rectangle r)
        {
            return LineIntersectsLine(p1, p2, new Point(r.X, r.Y), new Point(r.X + r.Width, r.Y)) ||
                   LineIntersectsLine(p1, p2, new Point(r.X + r.Width, r.Y), new Point(r.X + r.Width, r.Y + r.Height)) ||
                   LineIntersectsLine(p1, p2, new Point(r.X + r.Width, r.Y + r.Height), new Point(r.X, r.Y + r.Height)) ||
                   LineIntersectsLine(p1, p2, new Point(r.X, r.Y + r.Height), new Point(r.X, r.Y)) ||
                   (r.Contains(p1) && r.Contains(p2));
        }

        public static bool LineIntersectsLine(Point l1p1, Point l1p2, Point l2p1, Point l2p2)
        {
            float q = (l1p1.Y - l2p1.Y) * (l2p2.X - l2p1.X) - (l1p1.X - l2p1.X) * (l2p2.Y - l2p1.Y);
            float d = (l1p2.X - l1p1.X) * (l2p2.Y - l2p1.Y) - (l1p2.Y - l1p1.Y) * (l2p2.X - l2p1.X);

            if (d == 0)
            {
                return false;
            }

            float r = q / d;

            q = (l1p1.Y - l2p1.Y) * (l1p2.X - l1p1.X) - (l1p1.X - l2p1.X) * (l1p2.Y - l1p1.Y);
            float s = q / d;

            if (r < 0 || r > 1 || s < 0 || s > 1)
            {
                return false;
            }

            return true;
        }
    }
}
